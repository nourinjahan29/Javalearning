abstract class StudentResult {
    private String firstYearResult;
    private String secondYearResult;

    public void setFirstYearResult(String firstYearResult) {
        this.firstYearResult = firstYearResult;
    }
    public String getFirstYearResult () {
        return firstYearResult;
    }

    public void setSecondYearResult(String secondYearResult) {
        this.secondYearResult = secondYearResult;
    }

    public String getSecondYearResult() {
        return secondYearResult;
    }
}
